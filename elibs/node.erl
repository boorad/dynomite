-module(node).
-author('cliff@powerset.com').

%% API
-export([name/1, attributes/1]).

%%====================================================================
%% API
%%====================================================================
%%--------------------------------------------------------------------
%% @spec
%% @doc
%% @end
%%--------------------------------------------------------------------
name(Name) when is_atom(Name) ->
  Name;
name(Node) when is_tuple(Node) ->
  element(1, Node);
name(Node) ->
  Node.

attributes(Name) when is_atom(Name) ->
  [];
attributes(Node) when is_tuple(Node) ->
  element(2, Node);
attributes(_) ->
  [].
